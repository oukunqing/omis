        #region  获得一级汉字拼音
        private static string GetPinyin(int code)
        {
            if (code >= FirstCharCode && code <= LastOneLevelCharCode)
            {
                for (int i = CodeList.Length - 1; i >= 0; i--)
                {
                    if (CodeList[i] <= code)
                    {
                        return PinyinList[i];
                    }
                }
            }
            return string.Empty;
        }
        #endregion

        #region  获得二级汉字拼音
        private static string GetOtherPinyin(int code)
        {
            int idx = BinarySearch(OtherCodeList, code);
            if (idx >= 0)
            {
                return OtherPinyinList[idx];
            }
            return string.Empty;
        }
        #endregion

        #region  获得生僻字拼音
        private static string GetSpecialPinyin(int code)
        {
            int idx = BinarySearch(SpecialCodeList, code);
            if (idx >= 0)
            {
                return SpecialPinyinList[idx];
            }
            return string.Empty;
        }
