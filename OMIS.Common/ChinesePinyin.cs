        #region  二分查找
        public static int BinarySearch(int[] numArray, int num)
        {
            if (numArray.Length == 0)
            {
                return -1;
            }
            int left = 0;
            int right = numArray.Length - 1;
            int mid;

            while (left <= right)
            {
                mid = (left + right) / 2;
                if (numArray[mid] < num)
                {
                    left = mid + 1;
                }
                else if (numArray[mid] > num)
                {
                    right = mid - 1;
                }
                else
                {
                    return mid;
                }
            }
            return -1;
        }
