        #region  转换编码
        public static int ConvertCode(char c)
        {
            byte[] b = System.Text.Encoding.GetEncoding("GBK").GetBytes(c.ToString());
            return b.Length >= 2 ? (short)(b[0]) * 256 + (short)(b[1]) - 65536 : 1;
        }

        public static List<int> ConvertCode(string text)
        {
            char[] chars = text.ToCharArray();
            List<int> list = new List<int>();
            foreach (char c in chars)
            {
                list.Add(ConvertCode(c));
            }
            return list;
        }
