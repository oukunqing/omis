        #region  汉字转换拼音
        public static List<string> ConvertPinyin(string text)
        {
            List<string> result = new List<string>();

            char[] chars = text.ToCharArray();
            for (int j = 0, c = chars.Length; j < c; j++)
            {
                result.Add(ConvertPinyin(chars[j], ConvertCode(chars[j])));
            }
            return result;
        }

        public static string ConvertPinyin(char c)
        {
            return ConvertPinyin(c, ConvertCode(c));
        }

        public static string ConvertPinyin(char c, int code)
        {
            //标点符号、分隔符
            if (Char.IsPunctuation(c) || Char.IsSeparator(c))
            {
                return c.ToString();
            }
            //不检测是否在中文字符范围内，因有个别字的编码不在规定的范围内，如：(xin)编码为 -384
            /*
            //非中文字符
            if (c < '\x4E00' || c > '\x9FA5')
            {
                return c.ToString();
            }
            */

            //单字符--英文或半角字符 
            if (code > 0 && code < 160)
            {
                return c.ToString();
            }
            //先查找生僻字，因个别生僻字编码范围位于一级汉字编码中，如：盺(xin)编码为 -20130
            string py = GetSpecialPinyin(code);
            if (!py.Equals(string.Empty))
            {
                return py;
            }
            if (code >= FirstCharCode && code <= LastOneLevelCharCode)
            {
                return GetPinyin(code);
            }
            else
            {
                return GetOtherPinyin(code);
            }
        }
